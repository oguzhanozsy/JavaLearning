public class App {

    public static void main(String[] args) {
        
        /*
            KARŞILAŞTIRMA OPERATÖRLERİ
        
            == ---> Eşit mi ?
            != ---> Eşit değil mi ?
            > ---> Büyük mü ?
            >= ---> Büyük veya eşit mi ?
            < ---> Küçük mü ?
            <= ---> Küçük veya eşit mi ?
        */
        
        System.out.println(3 == 4);
        System.out.println(3 != 4);
        System.out.println(2 > 1);
        System.out.println(2 >= 3);
        System.out.println(2 < 1);
        System.out.println(2 <= 1);
        System.out.println("Oğuzhan" == "Oğuzhan");
    }
}
